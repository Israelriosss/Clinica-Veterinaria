    String sql = "SELECT * FROM adm";
    pst = con.prepareStatement(sql);
    rs = pst.executeQuery();

    // Obtém o modelo da tabela
    DefaultTableModel model = (DefaultTableModel) table.getModel();
    model.setRowCount(0); // Limpa a tabela antes de adicionar novos dados

